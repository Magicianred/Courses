#Conjuntos de variáveis ordenadas por uma rota

lista=[1,4,7,"Leticia",23,14]
print("Lista")
print(lista)

#inserir valor no final
lista.append("Python")
print("Lista")
print(lista)

#saber qual é a posição de um valor na lista.
print("Posição de Leticia na lista: ")
print(lista.index("Leticia"))

#Numero de variáveis do mesmo tipo na lista
print("Número de Leticia na lista: ")
print(lista.count("Leticia"))

#inserir valor no final
lista.append("Leticia")
print("Lista")
print(lista)

#Numero de variáveis do mesmo tipo na lista
print("Número de Leticia na lista: ")
print(lista.count("Leticia"))


#Remover valor
lista.remove("Python")
print("Lista remove Python")
print(lista)

#Colocar a lista ao contrario
lista.reverse()
print("Lista")
print(lista)

